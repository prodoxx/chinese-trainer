/**
 * Script to verify the correct structure of 朋友
 */

console.log('\n📝 Character Structure Analysis:');
console.log('   朋友 (péng yǒu) = friend');
console.log('   Components:');
console.log('     朋 (péng) = friend/companion');
console.log('     友 (yǒu) = friend');
console.log('\n   NOT to be confused with:');
console.log('     有 (yǒu) = to have/possess');
console.log('\n   The word 朋友 is composed of two characters that both mean "friend",');
console.log('   NOT 朋 + 有 (to have).');
console.log('\n   友 and 有 are different characters with the same pronunciation!');